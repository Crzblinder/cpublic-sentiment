    def get_playbook(self, risk_type: str) -> dict[str, Any]:
        """根据风险类型返回对应治理方案。

        Args:
            risk_type: 风险类型名称（如"产品质量"、"食品安全"等）

        Returns:
            结构化治理方案字典，未知类型返回通用方案
        """
        playbook = self.PLAYBOOKS.get(risk_type)
        if playbook:
            return dict(playbook)
        logger.warning("未知风险类型 '%s'，返回通用治理方案", risk_type)
        return dict(self._GENERIC_PLAYBOOK)

    def generate_summary(self, risk_type: str, content: str) -> str:
        """结合风险类型和内容生成治理摘要。

        Args:
            risk_type: 风险类型名称
            content: 事件内容文本

        Returns:
            治理摘要字符串
        """
        playbook = self.get_playbook(risk_type)
        base_summary = playbook.get("summary", "")
        immediate = playbook.get("immediate_actions", [])

        # 提取内容前 100 字作为事件概述
        content_brief = content[:100].replace("\n", " ").strip()
        if len(content) > 100:
            content_brief += "..."

        summary_parts = [
            f"【风险类型】{risk_type}",
            f"【事件概述】{content_brief}",
            f"【风险分析】{base_summary}",
        ]

        if immediate:
            summary_parts.append(f"【首要行动】{immediate[0]}")

        return "\n".join(summary_parts)
